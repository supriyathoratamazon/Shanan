package Conditional_statements;

public class Switch1 {
	public static void main(String[] args) {
		switch("friday") {
		case "monday" : {
			System.out.println("shiv mahadev");
			break;
		}
		case "tuesday":{
			System.out.println("laxmi");
			break;
		}
		case"wednesday":{
			System.out.println("datt");
			break;
		}
		default :{
			System.out.println("sunday");
			
		}
		}
	}

}
