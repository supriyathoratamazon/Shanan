package Conditional_statements;

public class If_else_ladder1 {
	public static void main(String[]args){
		int report=5;
		if (report>=90){
		System.out.println("vvvgood");
		}
		else if (report>=75){
		System.out.println("vvgood");
		}
		else if (report>=50){
		System.out.println("vgood");
		}
		else{
		System.out.println("fail");
		}
		}

}
